# diferente do pop que remove com base no índice da pilha, o remove atua sobre o objeto descrito em si.
# se a ocorrência é repetida, ele somente remove na ação um item por vez.

linguagens = ["python", "js", "c", "java", "csharp"]

linguagens.remove("c")

print(linguagens)  # ["python", "js", "java", "csharp"]
