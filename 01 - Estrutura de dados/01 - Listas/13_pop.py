# lista é organizada como pilha. como pilha de pratos, 1, 2, 3. Sem alterar a pilha removeria primeiro o 3 e assim por diante.
# o pop retira o último item da lista, ou posso indicar o índice do objeto que quero remover.

linguagens = ["python", "js", "c", "java", "csharp"]

print(linguagens.pop())  # csharp
print(linguagens.pop())  # java
print(linguagens.pop())  # c
print(linguagens.pop(0))  # python
